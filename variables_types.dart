void main(List<String> args) {
  /// this is integer
  int num1 = 100;

  /// this is double
  double num2 = 10.2;

  /// this is string
  String num3 = "the name is Rock";

  /// this is List, sometimes we call this array
  List num4 = ['Omar', 'Hasib', 'Sajid'];

  /// this is Map, in other languages we call it Dictionary
  Map num5 = {
    'tag': ['fiction'],
    'url': 'https://google.com',
  };

  /// final keyword will not let you change the value
  final num6 = 5;

  print(num1);
  print(num2);
  print(num3);
  print(num4);
  print(num5);
  print(num6);
}
